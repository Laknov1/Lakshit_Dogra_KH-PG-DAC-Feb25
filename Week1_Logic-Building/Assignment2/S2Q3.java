class S2Q3{
	public static void main(String args[]){
	int a = 5;
	int b = 5;
	String c = "-";
	if(c=="+"){
		System.out.println(a+b);
	}
	else if(c=="-"){
		System.out.println(a-b);
	}
	else if(c=="*"){
		System.out.println(a*b);
	}
	else if(c=="/"){
		System.out.println(a/b);
	}
	else{
		System.out.println("Invalid Input");
	}
	}
}